# round to the nearest whole number, up if even
round(7/4) # 2
round(6/4) # 2
round(5/4) # 1

# round up on the fraction
ceiling(7/4) # 2
ceiling(5/4) # 2

# round down on the fraction
floor(7/4) # 1
floor(5/4) # 1


# 7 / 4 is 1 with a remainder of 3

# division without remainder
7 %/% 4 # 1

# remainder
7 %% 4 # 3

# Square root
